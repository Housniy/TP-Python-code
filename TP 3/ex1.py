# Partie 1 : Définir la liste des nombres
nombres = [4, 8, 15, 16, 23, 42]

# Partie 2 : Définir une fonction pour afficher chaque élément de la liste
def affListe(liste):
    for element in liste:
        print(element)

# Appeler la fonction pour afficher les éléments de la liste
affListe(nombres)
